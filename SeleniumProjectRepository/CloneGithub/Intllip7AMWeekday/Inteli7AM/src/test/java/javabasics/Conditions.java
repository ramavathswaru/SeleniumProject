package javabasics;

public class Conditions {

    public static void main(String[] args) {

        int a = 55;
        int b = 67;

        if(a == b){
            System.out.println("Equal");
        }
        else if (a > b){
            System.out.println(" a is greater");
        }
        else if(a < b){
            System.out.println("b is greater");
        }
        else{
            System.out.println("Non Equal");
        }

    }
}
